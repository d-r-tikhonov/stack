#ifndef STACK_H
#define STACK_H

typedef int elem_t;

struct stack_t
{
	elem_t *data;
	size_t size;
	size_t capacity;
};

//=====================================================================================================================================

void stackCtor (stack_t* const stk);

//=====================================================================================================================================

void stackPush (stack_t* stk, const elem_t item);

//=====================================================================================================================================

elem_t stackPop (stack_t* const stk);

//=====================================================================================================================================

void stackDtor (stack_t* const stk);

//=====================================================================================================================================

void stackDump (const stack_t* const stk);


#endif